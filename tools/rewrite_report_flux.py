# Последний сохранённый индекс дочернего элемента body (0-based): пустой абзац перед «Введение»
LAST_KEEP_CHILD_INDEX = 29


def is_sect_pr(el) -> bool:
    return el.tag == qn("w:sectPr")


def strip_body_tail(doc: Document) -> None:
    body = doc.element.body
    to_remove = []
    for idx, child in enumerate(body):
        if is_sect_pr(child):
            continue
        if idx > LAST_KEEP_CHILD_INDEX:
            to_remove.append(child)
    for child in to_remove:
        body.remove(child)


def add_numbered_heading(doc: Document, title: str, level: str) -> None:
    doc.add_paragraph(title, style=level)


def add_plain(doc: Document, text: str) -> None:
    doc.add_paragraph(text, style="Normal")


def add_caption_centre(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text, style="Normal")
    p.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER


